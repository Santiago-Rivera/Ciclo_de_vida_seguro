using System;

namespace VariablesImplicitas
{
    internal static class VariablesImplicitas
    {
        static void Main()
        {
            // Declaración de una variable implícita

            var nombre = "Juan";

            var edad = 30;

            var altura = 1.75;

            var esEstudiante = true;

            var fechaNacimiento = new DateTime(1993, 5, 15);

            Console.WriteLine(nombre);

            Console.WriteLine(edad);

            Console.WriteLine(altura);

            Console.WriteLine(esEstudiante);

            Console.WriteLine(fechaNacimiento);
        }
    }
}
